package org.societies.api.ext3p.nzone.model;





public class ZoneDisplayDetail {

    protected int zoneNo ;
    protected int imageOffsetTopProfile;
    protected int imageOffsetLeftProfile;
    protected int imageOffsetTopOther;
    protected int imageOffsetLeftOther;
    protected String zoneName;
    
    
	public String getZoneName() {
		return zoneName;
	}
	public void setZoneName(String zoneName) {
		this.zoneName = zoneName;
	}
	public int getZoneNo() {
		return zoneNo;
	}
	public void setZoneNo(int zoneNo) {
		this.zoneNo = zoneNo;
	}
	public int getImageOffsetTopProfile() {
		return imageOffsetTopProfile;
	}
	public void setImageOffsetTopProfile(int imageOffsetTopProfile) {
		this.imageOffsetTopProfile = imageOffsetTopProfile;
	}
	public int getImageOffsetLeftProfile() {
		return imageOffsetLeftProfile;
	}
	public void setImageOffsetLeftProfile(int imageOffsetLeftProfile) {
		this.imageOffsetLeftProfile = imageOffsetLeftProfile;
	}
	public int getImageOffsetTopOther() {
		return imageOffsetTopOther;
	}
	public void setImageOffsetTopOther(int imageOffsetTopOther) {
		this.imageOffsetTopOther = imageOffsetTopOther;
	}
	public int getImageOffsetLeftOther() {
		return imageOffsetLeftOther;
	}
	public void setImageOffsetLeftOther(int imageOffsetLeftOther) {
		this.imageOffsetLeftOther = imageOffsetLeftOther;
	}
    
    
    
  
}
